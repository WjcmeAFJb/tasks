    override val licenseUrl: String
        get() = "https://github.com/google/material-design-icons/blob/master/LICENSE"

    override fun getIcon(key: String): IIcon = try {
        Icon.valueOf(key)
    } catch (e: IllegalArgumentException) {
        try {
            OutlinedGoogleMaterial3.getIcon(key)
        } catch (e2: IllegalArgumentException) {
            try {
                OutlinedGoogleMaterial4.getIcon(key)
            } catch (e3: IllegalArgumentException) {
                OutlinedGoogleMaterial2.getIcon(key)
